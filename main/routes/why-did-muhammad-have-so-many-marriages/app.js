const slideContent = document.getElementById("slideContent");
const nextBtn = document.getElementById("nextBtn");
const prevBtn = document.getElementById("prevBtn"); // اضافه شد

let currentSlide = 0;
let timerInterval;

function showSlide(index) {
  clearInterval(timerInterval);
  const slide = slides[index];
  let html = `<h2>${slide.title}</h2><p>${slide.content}</p>`;

  // هندل title2/content2, title3/content3 و غیره
  let i = 2;
  while (slide[`title${i}`] && slide[`content${i}`]) {
    html += `<h3>${slide[`title${i}`]}</h3><p>${slide[`content${i}`]}</p>`;
    i++;
  }

  slideContent.innerHTML = html;

  // وضعیت دکمه‌ها
  prevBtn.disabled = index === 0;
  if (index === slides.length - 1) {
    nextBtn.disabled = true;
    nextBtn.innerText = "رسیدیم به آخری"; // می‌تونی متن رو هم تغییر بدی
    return;
  }
  nextBtn.disabled = true;

  let remaining =
    slide.averageTime ||
    Math.ceil((slide.title + slide.content).split(/\s+/).length / 200);
  nextBtn.innerText = `بعدی (${remaining})`;

  timerInterval = setInterval(() => {
    remaining--;
    if (remaining > 0) {
      nextBtn.innerText = `بعدی (${remaining})`;
    } else {
      nextBtn.innerText = `بعدی`;
      nextBtn.disabled = false;
      clearInterval(timerInterval);
    }
  }, 1000);
}

// نمایش اسلاید اول
showSlide(currentSlide);

// رویداد کلیک Next
nextBtn.addEventListener("click", () => {
  if (currentSlide < slides.length - 1) {
    currentSlide++;
    showSlide(currentSlide);
  }
});

// رویداد کلیک Previous
prevBtn.addEventListener("click", () => {
  if (currentSlide > 0) {
    currentSlide--;
    showSlide(currentSlide);
  }
});
